import turtle
bob=turtle.Turtle()
#turtle.bgcolor(0,0,0)
bob.color('pink')
bob.width(5)
bob.speed(0)
for c in range(50):
  bob.circle(10*c)
  bob.up()
  bob.sety((10*c)*(-1))
  bob.down()

